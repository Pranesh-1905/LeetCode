class Solution:
    def orangesRotting(self, grid: List[List[int]]) -> int:
        q = deque()
        time, fresh = 0, 0
        r, c = len(grid), len(grid[0])
        for i in range(r):
            for j in range(c):
                if grid[i][j] == 1:
                    fresh += 1
                if grid[i][j] == 2:
                    q.append([i, j])
        dire = [[0, 1], [0, -1], [1, 0], [-1, 0]]
        while q and fresh > 0:
            for i in range(len(q)):
                row, col = q.popleft()
                for dr, dc in dire:
                    nr, nc = dr + row, dc + col
                    if (
                        nr < 0
                        or nr == len(grid)
                        or nc < 0
                        or nc == len(grid[0])
                        or grid[nr][nc] != 1
                    ):
                        continue
                    grid[nr][nc]=2
                    q.append([nr,nc])
                    fresh-=1
            time+=1
        return time if fresh==0 else -1
            

